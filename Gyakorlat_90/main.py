'''
Írj eljárást, amely paraméterül kapott számról eldönti, és a képernyőre kiírja, hogy negatív, pozitív vagy nulla-e!

'''


def dontes_hozo(szam):
    if szam < 0:
        print ("Ez a szám negatív! ")
    elif szam > 0:
        print ("Ez a szám pozitív!")
    else:
        print ("Ez a szám a 0 !")

dontes_hozo(-1)












